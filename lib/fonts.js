// índice numérico para .fonte#N
const indexed = ["bold","italic","bolditalic","smallcaps","fancy","monospace","doublestruck","serif","botoficia","gothic","script","outline","squared"];

function apply(text, style) {
  if (!text) return text;
  if (!style || style === "normal" || !maps[style]) return text;
  const { U, L } = maps[style];
  const uArr = Array.from(U), lArr = Array.from(L);
  return text.replace(/[A-Za-z]/g, (ch) => {
    const i = A.indexOf(ch); if (i >= 0 && uArr[i]) return uArr[i];
    const j = a.indexOf(ch); if (j >= 0 && lArr[j]) return lArr[j];
    return ch;
  });
}

function byIndex(n) {
  const i = parseInt(n) - 1;
  return indexed[i] || null;
}
function resolveName(s) {
  if (!s) return null;
  s = String(s).toLowerCase().replace(/^#/, "");
  if (/^\d+$/.test(s)) return byIndex(s);
  return maps[s] ? s : null;
}
function listAll() {
  return indexed.map((name, i) => ({ n: i+1, name, sample: apply("Flash-Bot", name) }));
}

module.exports = { apply, resolveName, listAll, styles: indexed.concat(["normal"]) };
